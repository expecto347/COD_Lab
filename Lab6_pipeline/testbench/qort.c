void swap(int* x, int* y) {
    int temp = *x;
    *x = *y;
    *y = temp;
}

void sort(int l, int r) {
    if (l == r-1) {
        if (nums[l] > nums[r])
            swap(&nums[l], &nums[r]);
        return;
    } else if (l >= r) {
        return;
    } else {
        int i = l, j = l;
        while (i < r) {
            if (nums[i] < nums[r]) {
                swap(&nums[i], &nums[j]);
                ++j;
            }
            ++i;
        }
        swap(&nums[i], &nums[j]);
        sort(l, j-1);
        sort(j+1, r);
    }
}

